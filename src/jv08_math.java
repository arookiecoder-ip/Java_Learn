public class jv08_math {
    public static void main(String[] args) {
        System.out.println(Math.PI); //value of pi
        System.out.println(Math.E);   //value of exponent

        double result;
        result = Math.pow(2,3); //power to m wrt n (m,n)


        System.out.println(result);

        double result1;
        result1= Math.abs(-5);  // absolute or mod value
        System.out.println(result1);

        double sqrt1;
        sqrt1 = Math.sqrt(9);
        System.out.println(sqrt1);


        double ceil1;
        ceil1 = Math.ceil(9.54);
        System.out.println(ceil1);

        double floor1;
        floor1 = Math.floor(9.54);
        System.out.println(floor1);

        double max1;
        max1 = Math.max(10,20);
        System.out.println(max1);

        double min;
        min = Math.min(10,20);
        System.out.println(min);
    }
}
